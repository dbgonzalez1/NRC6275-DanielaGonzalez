#Probar si un número es negativo
print('Probar si un número es negativo')
#Ingreso de valores por consola
numero = int(input("Escribe un número: "))
#Llama a la vaiable numero para verificar si este es menor que cero será NEGATIVO
if  numero < 0:
    print("El número",numero,"es Negativo")
    #Llama a la vaiable numero para verificar si este es mayor que cero será POSITIVO
else:
    numero > 0
    print("El número",numero,"es Positivo")